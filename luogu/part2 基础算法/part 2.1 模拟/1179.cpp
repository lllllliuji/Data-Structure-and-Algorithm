void solve() {
    int l, r;
    cin >> l >> r;
    int ans = 0;
    while (l <= r) {
        int x = l;
        l += 1;
        while (x) {
            if (x % 10 == 2) ans += 1;
            x /= 10;
        }
    }
    cout << ans << endl;
}

