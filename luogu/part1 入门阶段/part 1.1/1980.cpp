void solve() {
    int n, x;
    cin >> n >> x;
    int ans = 0;
    for (int i = 1; i <= n; i++) {
        int u = i;
        while (u) {
            int t = u % 10;
            if (t == x) ans++;
            u /= 10;
        }
    }
    cout << ans << endl;
}

